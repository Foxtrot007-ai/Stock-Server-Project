
public class Three_dimensional_parameters {
	private float x;
	private float y;
	private float z;
	public Three_dimensional_parameters(float a, float b, float c)
	{
		x = a;
		y = b;
		z = c;
	}
	
	public float get_x()
	{
		return x;
	}
	
	public float get_y()
	{
		return y;
	}
	
	public float get_z()
	{
		return z;
	}
}
